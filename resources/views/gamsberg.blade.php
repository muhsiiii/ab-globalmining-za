        <h5>Introduction</h5>
        <p style="text-align: justify;">ABGM was requested by Black Mountain Mining (Pty) Ltd
          (“BMM”) to undertake LOM scheduling and capital and
          operating cost estimations for their Gamsberg Zinc Open Pit
          Project.
          In 2014 BMM intended to establish the new Gamsberg zinc
          mine, ore benefication plant and associated infrastructure
          near the town of Aggeneys, Northern Cape Province, South
          Africa. Gamsberg is located between the towns of Aggeneys
          and Pofadder, approximately 120 km east of Springbok,
          along the N14 highway. The proposed site is characterized
          by an oval shaped Inselberg approximately 220 meters
          above the surrounding plains.
          The Gamsberg deposit is a low grade deposit with average
          zinc content of 6% Zinc associated with deleterious
          Manganese. Lead is an associated metal and may be of
          economic value. ABGM in conjunction with TCE (Tata
          Consulting Engineers) completed a techno economic PFS
          and then DFS accuracy study at +5%-10% during the 3rd
          quarter of 2014 for the Gamsberg Zinc Project</p>
