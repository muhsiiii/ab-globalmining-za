        <h5>Solution</h5>
        <p style="text-align: justify;">ABGM in conjunction with the mining team of BMM further
          developed and refined the V-Cut concept to facilitate
          reducing the amount of tramming and thereby reducing the
          overall cost per tonne. ABGM completed detailed design of
          the V-Cut and also of the North access ramp that connects
          with the V-Cut which allowed ore to be transported to the
          plant more economically.
